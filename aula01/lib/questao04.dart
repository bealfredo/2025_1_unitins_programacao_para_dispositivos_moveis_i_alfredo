// 4. Conversão de Tipos: Converta um número double para int e vice-versa.

void main() {
  double num1 = 1.0;
  const num2 = 2;

  int num1ToInt = num1.toInt();
  double num2ToDouble = num2.toDouble();

  print("Número 1: $num1");
  print("Número 2: $num2");
  print("Número 1 convertido para int: $num1ToInt");
  print("Número 2 convertido para double: $num2ToDouble");
}